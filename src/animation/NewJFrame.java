    public void movedPanelOnKeyBoard() throws InterruptedException {
        Point p = movedPanel.getLocation();
        if (movedPanelListener.isLeft) {
            if (p.x >= 0) {
                p.x -= 20;
                movedPanel.setLocation(p);
                movedPanelListener.isLeft = false;
            }
        }
        if (movedPanelListener.isRight) {
            if (p.x <= jPanel2.getWidth() - movedPanel.getWidth()) {
                p.x += 20;
                movedPanel.setLocation(p);
                movedPanelListener.isRight = false;
            }
        }
    }

