    public void moveBox() {
        Point p = jBox.getLocation();

        int coordXBox = p.x;
        int centerBox = p.x + jBox.getWidth() / 2;
        int rightSideBox = centerBox + jBox.getWidth() / 2;

        int coordXPan = movedPanel.getX();
        int borderXPan = movedPanel.getX() + movedPanel.getWidth();
        int coordYPan = movedPanel.getY();

        if (p.x >= jPanel2.getWidth() - jBox.getWidth() || (p.x <= 0))
            kx = -kx;
        if ((p.y <= 0))
            ky = -ky;


        if ((centerBox > coordXPan && rightSideBox < borderXPan && (p.y + jBox.getHeight() > coordYPan)) ||
                (coordXBox > coordXPan && centerBox < borderXPan && (p.y + jBox.getHeight() > coordYPan))) {

            if (p.y + jBox.getHeight() - coordYPan < 5) {

                score += 10;
                switch (score) {
                    case 20 -> thread.setDelay(15);
                    case 40 -> thread.setDelay(13);
                    case 60 -> thread.setDelay(11);
                    case 80 -> thread.setDelay(9);
                    case 100 -> thread.setDelay(50);
                    case 110 -> thread.setDelay(8);
                }

                String scoreStr = "" + score;
                jLabelScore.setText(scoreStr);
                ky = -ky;
            }
        }


        if (p.y >= jPanel2.getHeight()) {
            thread.canMove = false;
            this.dispose();
        }

        p.move(p.x + kx * STEP, p.y + ky * STEP);
        jBox.setLocation(p);
    }

